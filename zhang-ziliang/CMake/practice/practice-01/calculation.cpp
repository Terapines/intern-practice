#include"calculation.hpp"
// add 模板函数：实现两个值相加
 
int add(int lhs,int rhs) {
    return lhs + rhs;
}

// sub 模板函数：实现两个值相减
 
int sub(int lhs,int rhs) {
    return lhs - rhs;
}

// divide 模板函数：实现两个值相除
 
int divide(int lhs,int rhs) {
    return lhs / rhs;
}

// rem 模板函数：实现两个值相除的余数
 
int rem(int lhs,int rhs) {
    return lhs % rhs;
}
